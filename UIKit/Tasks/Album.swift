import Foundation

/// модель для хранения инфомации о песне
struct Album {
    // название песни для воспроизведения в mp3
    var song: String
    // изображение исполнителя
    var image: String
    // название трека
    var nameSong: String
    // имя исполнителя
    var nameArtist: String
    // длительность песни
    var time: String
}
